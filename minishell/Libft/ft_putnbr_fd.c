#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int	temp;

	temp = 0;
	if (n == -2147483648)
		write(fd, "-2147483648", 11);
	else
	{
		if (n < 0)
		{
			write(fd, "-", 1);
			n = n * -1;
		}
		if (n > 9)
			ft_putnbr_fd(n / 10, fd);
		temp = n % 10 + 48;
		write(fd, &temp, 1);
	}
}
