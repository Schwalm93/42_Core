#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int	value;

	value = 0;
	if (n == -2147483648)
		write (fd, "-2147483648", 11);
	if (n < 0 && n != -2147483648)
	{
		write(fd, "-", 1);
		n = n * -1;
	}
	if (n > 9)
		ft_putnbr_fd(n / 10, fd);
	if (n != -2147483648)
	{
		value = n % 10 + 48;
		write(fd, &value, 1);
	}
}
